package com.example.CampMastery.Model;

public class Bookmark {
    int id_user, id_bootcamp, id_bookmark;

    public int getId_user() {
        return id_user;
    }

    public void setId_user(int id_user) {
        this.id_user = id_user;
    }

    public int getId_bootcamp() {
        return id_bootcamp;
    }

    public void setId_bootcamp(int id_bootcamp) {
        this.id_bootcamp = id_bootcamp;
    }

    public int getId_bookmark() {
        return id_bookmark;
    }

    public void setId_bookmark(int id_bookmark) {
        this.id_bookmark = id_bookmark;
    }
}
